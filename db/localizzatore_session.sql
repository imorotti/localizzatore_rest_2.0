	(IDMAP VARCHAR2(100), DATA DATE, CONNSTRING VARCHAR2(100), MAPNAME VARCHAR2(50), MAPDESCRIPTION BLOB, IMAGEDISPLAY BLOB, SIMBOLOGIA BLOB DEFAULT NULL,
