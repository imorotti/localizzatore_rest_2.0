	(IDLRS VARCHAR2(100), DATA DATE, UTENTE VARCHAR2(50), PWD VARCHAR2(50), CONNSTRING VARCHAR2(100), ROADLAYER VARCHAR2(50), GEOMETRYCOL VARCHAR2(50), IDCOL VARCHAR2(50),
