	);
	
CREATE TABLE NORMALIZZATORE_SESSION
	(HANDLE VARCHAR2(100), DATA DATE, NORMALIZZATORESOAPPORT BLOB,
	 CONSTRAINT PK_NORMALIZZATORE_SESSION primary key (HANDLE)
	);
	
